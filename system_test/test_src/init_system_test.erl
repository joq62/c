-define(TEST_BOARDS_CONF,"test_boards.conf").


%% External exports

-export([start/0]).


%% ====================================================================
%% External functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Function:init 
%% Description:
%% Returns: non
%% --------------------------------------------------------------------
start()->
    % Check if all test nodes are running
    {ok,I}=file:consult(?TEST_BOARDS_CONF),
    Ping_1=[{Node,net_adm:ping(Node)}||Node<-I],
    Ping_2=[{error,Node,R}||{Node,R}<-Ping_1,R/=pong],
    Result = case Ping_2 of
		 []->
		     ok;
		 Ping_2->
		     {error,Ping_2}
	     end,
    Result.


